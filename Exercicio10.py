#Exercicio10: Verificador de Maioridade.
# Verifica se a pessoa é maior de idade ou não.
idade = int(input("Digite a sua idade: "))
if idade >= 18:
    print("Você é maior de idade.")
else:
    print("Você é menor de idade.")
# Explicação:
# 1. A entrada do usuário é lida como um inteiro.
# 2. Verificamos se a idade é maior ou igual a 18.
# 3. Se for, imprimimos que a pessoa é maior de idade; caso contrário, imprimimos que é menor de idade.
# Observação: O código acima assume que a idade é um número inteiro positivo.
